// == (comparação implícita)

const numero = 5;
const texto = "5";

console.log(numero == texto) // == só compara o valor
console.log(numero === texto) // === compara o valor + o tipo de dado

//typeof
console.log(typeof numero) // typeof remove o tipo de dado
console.log(typeof texto) // typeof remove o tipo de dado


